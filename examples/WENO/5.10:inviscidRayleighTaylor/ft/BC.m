function Q=BC(Q,x,y,z,t,gam)
    %
    % X=0 (reflective)
    %
    Q(1,1:3,:,:) =  Q(1,[6 5 4],:,:);
    Q(2,1:3,:,:) = -Q(2,[6 5 4],:,:);
    Q(3,1:3,:,:) =  Q(3,[6 5 4],:,:);
    Q(4,1:3,:,:) =  Q(4,[6 5 4],:,:);
    Q(5,1:3,:,:) =  Q(5,[6 5 4],:,:);
    %
    % X=0.25 (reflective)
    %
    Q(1,end-2:end,:,:) =  Q(1,[end-3 end-4 end-5],:,:);
    Q(2,end-2:end,:,:) = -Q(2,[end-3 end-4 end-5],:,:);
    Q(3,end-2:end,:,:) =  Q(3,[end-3 end-4 end-5],:,:);
    Q(4,end-2:end,:,:) =  Q(4,[end-3 end-4 end-5],:,:);
    Q(5,end-2:end,:,:) =  Q(5,[end-3 end-4 end-5],:,:);
    %
    % Y=0 (from IC)
    %
    Q(1,:,1:3,:) = 2;
    Q(2,:,1:3,:) = 0;
    Q(3,:,1:3,:) = 0;
    Q(4,:,1:3,:) = 0;
    Q(5,:,1:3,:) = 1/(gam-1);
    %
    % Y=1 (from IC)
    %
    Q(1,:,end-2:end,:) = 1;
    Q(2,:,end-2:end,:) = 0;
    Q(3,:,end-2:end,:) = 0;
    Q(4,:,end-2:end,:) = 0;
    Q(5,:,end-2:end,:) = 2.5/(gam-1);
    %
    % Z (periodic)
    %
    Q(:,:,:,1:3)       = Q(:,:,:,end-5:end-3);
    Q(:,:,:,end-2:end) = Q(:,:,:,4:6);
    %
end